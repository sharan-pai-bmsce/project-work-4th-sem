<li><strong>What is a conference paper?</strong></li><br>
Conference papers refer to articles that are written with the goal of being accepted to a conference: typically an annual (or biannual) venue with a specific scope where you can present your results to the community, usually as an oral presentation, a poster presentation, or a tabled discussion. The review process for conference papers is typically within a fixed window: everyone submits for a certain deadline, then the review committee (program committee) collaborates to review and discuss papers, then all authors are notified with accept/reject at the same time. Since the review process has a fixed schedule (to meet the schedule of the physical meeting), conference review times are quite predictable.<br>
<br><li><strong>What is the importance of conference papers?</strong></li><br>
In a conference paper an author or an associated worker gets a chance to put their ‘cards on the table’ on a particular topic. If it is a conference well attended on your paper topic it gives you an opportunity to stake your claim to some new ideas you may have on the topic. If your paper is a review of the topic, it also gives you the opportunity to give credit to members of the audience to whom you want to show respect for their contributions that lead to your new ideas. Your paper may stimulate discussion immediately after your presentation if you give enough time for discussion<br><br>
<li><strong> What is the Difference between a Conference Paper and a Journal Paper ?
</strong></li><br>
Almost all researchers have been there. The amount of confusion between the two has only been increasing in this Internet age where access to journals and conferences alike is readily available online.

conference paper: The papers submitted to a conference are usually reviewed during a specific period and authors receive their acceptance or rejection letters at the same time. Conference papers are usually short and concise with a limit on the number of pages allowed.

journal paper :  For journal papers, on the other hand, the amount of time needed for publishing is very flexible. If your paper is promising but there are edits required, there could be a lot of back and forth between you and your editor until your paper is ready to be published.<br><br>
<li><strong>How a conference paper should be presented ?</strong></li><br>
A technical conference consists of blocks of presentations in which the material being presented is on the same topic; these blocks are called technical sessions. Each technical session is headed by a Session Chairman and is organized by a Session Organizer. It is the Session Organizer who makes the actual arrangements with authors of papers intended for that session. As a rule, three to six papers are presented at each technical session. Twenty minutes presentation time per paper is the usual allotment, but this may vary to allow for adequate presentation or discussion of the material. <br>
English is normally the official language for paper presentation. If an author is not sufficiently fluent in English to present his paper, or if an author cannot present his paper for any other reason, it is mandatory that mutually agreed upon arrangements be made with the Session Organizer (or his designated representative) for a substitute presenter. If such arrangements are not made, the paper will be rejected.</ol>

